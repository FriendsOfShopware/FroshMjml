#!/usr/bin/env node
/**
 * Generates src/component/frosh-mjml-editor/mjml-tags.js from the installed
 * `mjml` package by introspecting each component's static `allowedAttributes`.
 *
 * Run: npm run generate-mjml-tags
 */
import { createRequire } from 'node:module';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { writeFileSync } from 'node:fs';

const require = createRequire(import.meta.url);
const __dirname = dirname(fileURLToPath(import.meta.url));

require('mjml');
const { components } = require('mjml-core');

const mjmlPkg = require('mjml/package.json');

const names = Object.keys(components).sort();

const tags = {};
for (const name of names) {
    const cls = components[name];
    const allowed = (cls && cls.allowedAttributes) || {};
    tags[name] = Object.keys(allowed).sort();
}

// Root <mjml> element is not in mjml-core components (it's parsed by mjml-parser-xml).
// Inject manually.
if (!tags.mjml) {
    tags.mjml = ['owa', 'lang', 'dir'];
}

// Reorder so 'mjml' sorts first, rest alphabetical.
const ordered = {};
ordered.mjml = tags.mjml;
for (const name of Object.keys(tags).sort()) {
    if (name !== 'mjml') ordered[name] = tags[name];
}

const lines = [];
lines.push('/* eslint-disable */');
lines.push(
    `// Auto-generated by scripts/generate-mjml-tags.mjs from mjml@${mjmlPkg.version}.`
);
lines.push(
    '// Do not edit by hand. Run: `npm run generate-mjml-tags` in side src/Resources/app/administration'
);
lines.push('');
lines.push('export const MJML_TAGS = {');
for (const [tag, attrs] of Object.entries(ordered)) {
    if (attrs.length === 0) {
        lines.push(`    '${tag}': [],`);
        continue;
    }
    lines.push(`    '${tag}': [`);
    for (const attr of attrs) {
        lines.push(`        '${attr}',`);
    }
    lines.push('    ],');
}
lines.push('};');
lines.push('');

const outPath = resolve(
    __dirname,
    '..',
    'src',
    'component',
    'frosh-mjml-editor',
    'mjml-tags.js'
);
writeFileSync(outPath, lines.join('\n'));

const totalAttrs = Object.values(ordered).reduce(
    (sum, attrs) => sum + attrs.length,
    0
);
console.log(`Wrote ${outPath}`);
console.log(
    `mjml@${mjmlPkg.version} — ${Object.keys(ordered).length} tags, ${totalAttrs} attrs`
);
